    private void fillLecturerList() 
    {
        String[] lecturers = admin.getAllLecturers().keySet().toArray(new String[admin.getAllLecturers().size()]);
        lecturerList.setModel(new DefaultComboBoxModel<>(lecturers));
    }

    private void fillSubjectList() 
    {
        subjectList.setModel(new javax.swing.AbstractListModel<String>() {
            String[] subjects = admin.getAvailableSubjects(admin.getUsername(), admin.getPassword()).keySet().toArray(new String[admin.getAvailableSubjects(admin.getUsername(),admin.getPassword()).size()]);
            public int getSize() { return subjects.length; }
            public String getElementAt(int i) { return subjects[i]; }
        });
    }
