    private void assignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignButtonActionPerformed
        // TODO add your handling code here:
        List<String> subjects =subjectList.getSelectedValuesList();
        String lecID = (String) lecturerList.getSelectedItem();
        if(subjects.size()>0&&lecID!=null)
        {
            for(String sjCode:subjects)
            {
                if(!admin.assignSubject(admin.getUsername(), admin.getPassword(), sjCode, lecID))
                {
                    JOptionPane.showMessageDialog(null,"CAN'T Assign subject "+sjCode+" to lecturer "+lecID);
                } else
                {
                    JOptionPane.showMessageDialog(null,"Assign subject "+sjCode+" to lecturer "+lecID+" successfully!");
                }
            }
        }
        fillSubjectList();
        
    }//GEN-LAST:event_assignButtonActionPerformed

